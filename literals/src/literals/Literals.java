package literals;

/**
 * 
 * @author Colbehr 
 * assignment literals
 * 
 */
public class Literals {
	public static void main(String[] args){
		System.out.println("here is one line. \n and here is another.");
		System.out.println("This line has tabs \t\t\t in it");
		System.out.println("what happens if I include \\\\\\ in my string?");
		System.out.println("33 + 9");
		
		System.out.println(33 + 9);
		System.out.println(16 / 4);
		System.out.println(16 / 5);
		System.out.println((13 - 6) * (8 / 4));
		
		System.out.println(15.0 + 6.9);
		System.out.println(16.0 / 4.0);
		System.out.println(16.0 / 5.);
		System.out.println((18.2 / 2.0) + 6.5);
}
}
