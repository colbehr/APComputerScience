			System.out.println("BB     BB");
			System.out.println("BB     BB");
			System.out.println("BBBBBBBB");
			System.out.println("BB     BB");
			System.out.println("BB     BB");
			System.out.println("BBBBBBBBB");
			System.out.println("BBBBBBBB");
			System.out.println();
			System.out.println();
// 3 Letters Horizontal
			System.out.print("CCCCCCCCC    JJJJJJ BBBBBBBB");
			System.out.println();
			System.out.print("CCCCCCCCC    JJJJJJ BBBBBBBBB");
			System.out.println();
			System.out.print("CC               JJ BB     BB");
			System.out.println();
			System.out.print("CC               JJ BB     BB");
