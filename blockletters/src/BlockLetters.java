			System.out.println("CC       ");
			System.out.println("CC       ");
			System.out.println("CC       ");
			System.out.println("CCCCCCCCC");
			System.out.println("CCCCCCCCC");

			System.out.println();
			System.out.println("   JJJJJJ");
			System.out.println("   JJJJJJ");
