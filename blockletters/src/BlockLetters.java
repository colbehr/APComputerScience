			System.out.println("       JJ");
			System.out.println("       JJ");
			System.out.println("       JJ");
			System.out.println("JJ     JJ");
			System.out.println("JJJJJJJJJ");
			System.out.println("JJJJJJJJJ");

			System.out.println();
			System.out.println("BBBBBBBB");
