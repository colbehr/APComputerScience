			System.out.print("CC               JJ BBBBBBBB");
			System.out.println();
			System.out.print("CC               JJ BB     BB");
			System.out.println();
			System.out.print("CC        JJ     JJ BB     BB");
			System.out.println();
			System.out.print("CCCCCCCCC JJJJJJJJJ BBBBBBBBB");
			System.out.println();
			System.out.print("CCCCCCCCC JJJJJJJJJ BBBBBBBB");
			System.out.println();
			System.out.println();
// or the shorter easier way
